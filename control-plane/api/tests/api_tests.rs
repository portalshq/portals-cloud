#[sqlx::test(migrations = "../persistence/migrations")]
#[ignore]
async fn organization_soft_delete_returns_202(pool: sqlx::PgPool) {
    let app = test_app(pool);
    
    // Create organization
    let create_response = app
        .clone()
        .oneshot(
            Request::builder()
                .method("POST")
                .uri("/api/v1/organizations")
                .header("content-type", "application/json")
                .body(Body::from(
                    serde_json::to_vec(&serde_json::json!({
                        "name": "Test Org",
                        "slug": "test-org"
                    }))
                    .unwrap(),
                ))
                .unwrap(),
        )
        .await
        .unwrap();
    assert_eq!(create_response.status(), StatusCode::CREATED);

    let body = create_response.into_body().collect().await.unwrap().to_bytes();
    let json: serde_json::Value = serde_json::from_slice(&body).unwrap();
    let org_id = json["id"].as_str().unwrap();

    // Delete organization (soft delete)
    let delete_response = app
        .oneshot(
            Request::builder()
                .method("DELETE")
                .uri(&format!("/api/v1/organizations/{}", org_id))
                .body(Body::empty())
                .unwrap(),
        )
        .await
        .unwrap();
    assert_eq!(delete_response.status(), StatusCode::ACCEPTED);
}

#[sqlx::test(migrations = "../persistence/migrations")]
#[ignore]
async fn organization_with_repositories_cannot_delete(pool: sqlx::PgPool) {
    let app = test_app(pool);
    
    // Create organization
    let create_org_response = app
        .clone()
        .oneshot(
            Request::builder()
                .method("POST")
                .uri("/api/v1/organizations")
                .header("content-type", "application/json")
                .body(Body::from(
                    serde_json::to_vec(&serde_json::json!({
                        "name": "Test Org",
                        "slug": "test-org"
                    }))
                    .unwrap(),
                ))
                .unwrap(),
        )
        .await
        .unwrap();
    assert_eq!(create_org_response.status(), StatusCode::CREATED);

    let org_body = create_org_response.into_body().collect().await.unwrap().to_bytes();
    let org_json: serde_json::Value = serde_json::from_slice(&org_body).unwrap();
    let org_id = org_json["id"].as_str().unwrap();

    // Create repository in organization
    let create_repo_response = app
        .clone()
        .oneshot(
            Request::builder()
                .method("POST")
                .uri("/api/v1/repositories")
                .header("content-type", "application/json")
                .body(Body::from(
                    serde_json::to_vec(&serde_json::json!({
                        "name": "test-repo",
                        "storage_tier": "standard",
                        "organization": org_id
                    }))
                    .unwrap(),
                ))
                .unwrap(),
        )
        .await
        .unwrap();
    assert_eq!(create_repo_response.status(), StatusCode::CREATED);

    // Try to delete organization (should fail due to active repository)
    let delete_response = app
        .oneshot(
            Request::builder()
                .method("DELETE")
                .uri(&format!("/api/v1/organizations/{}", org_id))
                .body(Body::empty())
                .unwrap(),
        )
        .await
        .unwrap();
    // Should return 202 (accepted for deletion) but the reconciler will fail to finalize
    // This is expected behavior - the deletion is accepted but will fail during finalization
    assert_eq!(delete_response.status(), StatusCode::ACCEPTED);
}
