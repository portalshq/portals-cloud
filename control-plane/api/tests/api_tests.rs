// ============================================================

#[sqlx::test(migrations = "../persistence/migrations")]
#[ignore]
async fn idempotency_caches_successful_response(pool: sqlx::PgPool) {
    let store = Arc::new(persistence::PostgresStateStore::new(pool));
    let (_, signing_key_b64) = api::auth::DataPlaneSigningKey::generate();
    let signing_key = api::auth::DataPlaneSigningKey::from_env(&signing_key_b64)
        .expect("failed to create signing key from generated bytes");

    let state = api::http::AppState {
        store,
        signing_key,
        dp_token_expiry_secs: 3600,
    };

    // Enable idempotency
    let app = api::http::build_router(state, false, true, None);

    let idempotency_key = "test-key-123";

    // First request
    let response1 = app
        .clone()
        .oneshot(
            Request::builder()
                .method("POST")
                .uri("/api/v1/organizations")
                .header("content-type", "application/json")
                .header("idempotency-key", idempotency_key)
                .body(Body::from(
                    serde_json::to_vec(&serde_json::json!({
                        "name": "Test Org",
                        "slug": "test-org"
                    }))
                    .unwrap(),
                ))
                .unwrap(),
        )
        .await
        .unwrap();
    assert_eq!(response1.status(), StatusCode::CREATED);

    let body1 = response1.into_body().collect().await.unwrap().to_bytes();
    let json1: serde_json::Value = serde_json::from_slice(&body1).unwrap();
    let id1 = json1["id"].as_str().unwrap();

    // Second request with same idempotency key
    let response2 = app
        .oneshot(
            Request::builder()
                .method("POST")
                .uri("/api/v1/organizations")
                .header("content-type", "application/json")
                .header("idempotency-key", idempotency_key)
                .body(Body::from(
                    serde_json::to_vec(&serde_json::json!({
                        "name": "Different Org",
                        "slug": "different-org"
                    }))
                    .unwrap(),
                ))
                .unwrap(),
        )
        .await
        .unwrap();
    assert_eq!(response2.status(), StatusCode::CREATED);

    let body2 = response2.into_body().collect().await.unwrap().to_bytes();
    let json2: serde_json::Value = serde_json::from_slice(&body2).unwrap();
    let id2 = json2["id"].as_str().unwrap();

    // Should return the same ID (cached response)
    assert_eq!(id1, id2);
    assert_eq!(json2["name"], "Test Org"); // Original name, not "Different Org"
}

