/// Build a test Router backed by a real Postgres database.
/// Uses sqlx::test which creates a temporary database per test.
fn test_app(pool: sqlx::PgPool) -> axum::Router {
    let store = Arc::new(persistence::PostgresStateStore::new(pool));
    let (_, signing_key_b64) = api::auth::DataPlaneSigningKey::generate();
    let signing_key = api::auth::DataPlaneSigningKey::from_env(&signing_key_b64)
        .expect("failed to create signing key from generated bytes");

    let state = api::http::AppState {
        store,
        signing_key,
        dp_token_expiry_secs: 3600,
    };

    api::http::build_router(state, false, false, None)
}

